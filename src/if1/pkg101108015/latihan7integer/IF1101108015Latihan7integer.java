public class IF1101108015Latihan7integer {

    public static void main(String[] args) {
        byte b;
        short s;
        int i;
        long l;
        b = 120;
        s = 32767;
        i = 1_000_000_000;
        l = 900000000000L;
        System.out.println("byte : "+ b);
        System.out.println("short integer : " + s);
        System.out.println("integer : " + i);
        System.out.println("long integer : " + l);
    }
    
}
